package com.msibai.cloud.entities;

/** Enumeration representing different roles that users can have within the system. */
public enum Role {
  /**
   * Represents the role of an administrator. Users with this role typically have administrative
   * privileges and permissions within the system.
   */
  ROLE_ADMIN,

  /**
   * Represents the role of a regular user. Users with this role have standard access and
   * functionalities within the system, without administrative privileges.
   */
  ROLE_USER
}
